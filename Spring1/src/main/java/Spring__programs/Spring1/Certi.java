package Spring__programs.Spring1;

public class Certi {
	
	private String name;
	private String type;
	@Override
	public String toString() {
		return "Certi [name=" + name + ", type=" + type + "]";
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

}
